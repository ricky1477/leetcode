# Longest Substring Without Repeating Characters

# Given a string, find the length of the longest substring without repeating characters.

# Example 1:

# Input: "abcabcbb"
# Output: 3 
# Explanation: The answer is "abc", with the length of 3. 
# Example 2:

# Input: "bbbbb"
# Output: 1
# Explanation: The answer is "b", with the length of 1.
# Example 3:

# Input: "pwwkew"
# Output: 3
# Explanation: The answer is "wke", with the length of 3. 
#              Note that the answer must be a substring, "pwke" is a subsequence and not a substring.


# @param {String} s
# @return {Integer}
# Sliding window problem to avoid brute force inneficiency
def length_of_longest_substring(s)
  return s.length if s.length < 2
  cur_substr = ''
  longest_substring = ''

  s.each_char do |ch|
    cur_substr += ch
    if cur_substr.count(ch) > 1
      cur_substr = cur_substr[cur_substr.index(ch)+1,cur_substr.length]
    end
    longest_substring = cur_substr if cur_substr.length > longest_substring.length
  end
  longest_substring.length
end

