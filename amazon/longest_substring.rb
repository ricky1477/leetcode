# Longest Substring Without Repeating Characters

# Given a string, find the length of the longest substring without repeating characters.

# Example 1:

# Input: "abcabcbb"
# Output: 3 
# Explanation: The answer is "abc", with the length of 3. 
# Example 2:

# Input: "bbbbb"
# Output: 1
# Explanation: The answer is "b", with the length of 1.
# Example 3:

# Input: "pwwkew"
# Output: 3
# Explanation: The answer is "wke", with the length of 3. 
#              Note that the answer must be a substring, "pwke" is a subsequence and not a substring.

def longest_substring(s)
  return 0 if s.empty?
  substrings = Array.new
  #sbstr = ''
  s.each_char.with_index do |char, j|
    sbstr = ''
    inner_str = s[j..s.length-1]
    #p "--#{inner_str}--"
    inner_str.each_char.with_index do |ch, i|
      unless (sbstr.include?(ch))
        sbstr << ch
        #p "#{sbstr}|#{ch}"
        # If also last char of input s 
        substrings << sbstr if (i == inner_str.length - 1)
      else
        substrings << sbstr
        sbstr = ch
      end
    #p sbstr
    end
  end
  puts
  puts "#{s} | #{substrings.inspect}"
  return substrings.max_by(&:length).length
end

def longest_substring(s)
  return s.size if s.size < 2
      
  queue, hash = [], {}
  s.each_char.reduce(-Float::INFINITY) do |max, c|
    hash.delete(char = queue.shift) until queue.empty? || char.eql?(c) if hash.key?(c)
    [max, (queue << hash[c] = c).size].max
  end
end

